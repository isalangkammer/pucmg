/* Calcular a soma dos númerios de 0 até 100 
 */
import IO.*;
public class somadosnumerosinteiros{
      public static void main(String[]args){
             int x,s=0;
             for(x=0;x<=100;x++){
                 s=s+x;
             }
             System.out.println("A soma dos números inteiros de 0 a 100 é :"+s);
      }//fim main()
}//fim classe somadosnumerosinteiros   