import IO.*;
public class estruturaencadeada{
      public static void main(String[]args){
            int i,j;
            for(i=0;i<4;i++){
               for(j=0;j<4;j++){
                  System.out.print(+i+"\t"+j+"\n");
               }
               System.out.print("\n");
            }
      }//fim main
}//fim classe